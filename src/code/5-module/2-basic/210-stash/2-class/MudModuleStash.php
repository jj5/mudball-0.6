class MudModuleStash extends MudModuleBasic {


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2024-07-30 jj5 - private fields...
  //

  private array $stash_map = [];


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2024-07-30 jj5 - public methods...
  //

  public function get_stash( string $name ) : MudStash {

    if ( ! isset( $this->stash_map[ $name ] ) ) {

      $this->stash_map[ $name ] = $this->new_stash( $name );

    }

    return $this->stash_map[ $name ];

  }


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2024-07-30 jj5 - protected methods...
  //

  protected function new_stash( $name ) {

    return MudStash::Create( $name );

  }
}
