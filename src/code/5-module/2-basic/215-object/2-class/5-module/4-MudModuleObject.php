class MudModuleObject extends MudModuleThing {


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2024-07-01 jj5 - mixins...
  //

  use MudHostMixin;


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2024-07-01 jj5 - IMudHost interface...
  //

  public function get_child_list() : array { return $this->child_list; }


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2024-07-01 jj5 - protected instance methods...
  //

  protected array $child_list = [];

  protected function add_object( IMudObject $obj ) : void {

    $this->child_list[] = $obj;

  }
}
