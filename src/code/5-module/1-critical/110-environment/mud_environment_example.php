declare_examples([


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2021-03-04 jj5 - is cli
  //

  'is cli' => function() {

    $is_cli = mud_is_cli();

    if ( $is_cli ) { echo "This is a command-line process.\n"; }

    return 0;

  },


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2021-03-04 jj5 - is web
  //

  'is web' => function() {

    $is_web = mud_is_web();

    if ( $is_web ) { echo "This is a web process.\n"; }

    return 0;

  },


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2021-03-04 jj5 - is posix
  //

  'is posix' => function() {

    $is_posix = mud_is_posix();

    if ( $is_posix ) { echo "This process has the POSIX functions.\n"; }

    return 0;

  },


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2021-03-04 jj5 - is windows
  //

  'is windows' => function() {

    $is_windows = mud_is_windows();

    if ( $is_windows ) { echo "This process is running on Microsoft Windows.\n"; }

    return 0;

  },

]);
