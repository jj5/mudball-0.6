<?php

class MudModuleSession extends MudModuleWeb {


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2024-09-29 jj5 - protected fields...
  //

  protected $session = null;


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2024-09-29 jj5 - public methods...
  //

  public function get_session() : MudSession {

    if ( ! $this->session ) {

      $this->session = $this->new_session();

    }

    return $this->session;

  }


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2024-09-29 jj5 - protected methods...
  //

  protected function new_session() : MudSession {

    return MudSessionPhp::Create();

  }
}
